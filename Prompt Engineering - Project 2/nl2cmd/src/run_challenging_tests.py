import csv
from agent import nl_to_cmd

# תרחישים מאתגרים - יצירתיים, ארוכים, דו-משמעיים
challenging_cases = [
    # דו-משמעיים
    "תמחק לי את הקבצים הישנים",
    "אני רוצה לראות מה קורה במחשב",
    "תנקה את המערכת",
    "בדוק אם יש בעיות",
    
    # מרובי הוראות
    "תצור תיקייה בשם backup ואז תעתיק לשם את כל הקבצים מהתיקייה הנוכחית",
    "אני רוצה למחוק את התיקייה temp ואחר כך ליצור אותה מחדש",
    "הצג לי את כל התהליכים ואז תרוג את notepad",
    
    # ארוכים ומפורטים
    "אני צריך למצוא את כל הקבצים עם סיומת .log שנוצרו בשבוע האחרון בכל התיקיות במחשב ולמחוק אותם",
    "תחפש לי בכל הדיסקים קבצים שגדולים מ-100MB ותציג אותם ממוין לפי גודל",
    "אני רוצה לראות את כל הקבצים בתיקייה documents שהשם שלהם מכיל את המילה report והם נוצרו החודש",
    
    # לא ברורים
    "עזור לי למצוא את הקובץ",
    "משהו לא עובד עם הרשת",
    "המחשב איטי",
    
    # שפה לא פורמלית
    "יאללה תמחק לי את כל הזבל מהדאונלודס",
    "בוא נראה מה יש פה בתיקייה",
    "תעיף לי את הנוטפד",
    
    # מערבים מושגים לא טכניים
    "תראה לי כמה מקום נשאר לי בדיסק",
    "אני רוצה לגבות את הקבצים החשובים",
    "תבדוק אם האינטרנט עובד",
    
    # משימות מורכבות
    "צור עותק של כל הקבצים מהתיקייה הנוכחית לתיקייה backup אבל רק אם הם נוצרו היום",
    "מצא את כל התהליכים שצורכים יותר מ-100MB זיכרון",
]

results = []

print("מריץ תרחישים מאתגרים...\n")

for i, test_input in enumerate(challenging_cases, 1):
    print(f"[{i}/{len(challenging_cases)}] {test_input}")
    try:
        output = nl_to_cmd(test_input)
        results.append({
            "מספר": i,
            "קלט": test_input,
            "פלט": output,
            "סוג אתגר": "",
            "סטטוס": "",
            "הערות": ""
        })
        print(f"    → {output}\n")
    except Exception as e:
        results.append({
            "מספר": i,
            "קלט": test_input,
            "פלט": f"ERROR: {str(e)}",
            "סוג אתגר": "",
            "סטטוס": "שגוי",
            "הערות": "שגיאה בהרצה"
        })
        print(f"    → ERROR: {e}\n")

with open("challenging_results.csv", "w", newline="", encoding="utf-8-sig") as f:
    writer = csv.DictWriter(f, fieldnames=["מספר", "קלט", "פלט", "סוג אתגר", "סטטוס", "הערות"])
    writer.writeheader()
    writer.writerows(results)

print(f"\n✅ הושלם! התוצאות נשמרו ב-challenging_results.csv")
print(f"📊 סה\"כ תרחישים מאתגרים: {len(challenging_cases)}")
